// Related words map for generating taboo words (sample - you can expand this)
const relatedWords: Record<string, string[]> = {
  'DOG': ['ANIMAL', 'PET', 'BARK', 'PUPPY', 'CAT'],
  'CAT': ['ANIMAL', 'PET', 'MEOW', 'KITTEN', 'DOG'],
  'APPLE': ['FRUIT', 'RED', 'TREE', 'EAT', 'FOOD'],
  'WATER': ['DRINK', 'LIQUID', 'WET', 'OCEAN', 'BLUE'],
  'PHONE': ['CALL', 'MOBILE', 'DEVICE', 'SCREEN', 'IPHONE'],
  'LOVE': ['HEART', 'LIKE', 'ROMANCE', 'FEELING', 'EMOTION'],
  'SCHOOL': ['STUDENT', 'TEACHER', 'EDUCATION', 'CLASS', 'LEARN'],
  'FOOD': ['EAT', 'MEAL', 'HUNGRY', 'COOK', 'RESTAURANT'],
  'MOVIE': ['FILM', 'CINEMA', 'WATCH', 'ACTOR', 'HOLLYWOOD'],
  'MUSIC': ['SONG', 'SING', 'SOUND', 'LISTEN', 'PLAY'],
}

// Generate taboo words based on the main word
function generateTabooWords(word: string): string[] {
  const upperWord = word.toUpperCase()
  
  // If we have predefined related words, use them
  if (relatedWords[upperWord]) {
    return relatedWords[upperWord]
  }
  
  // Otherwise, generate generic taboo words
  const tabooWords: string[] = []
  const words = word.split(' ')
  
  // Add individual words from multi-word entries
  if (words.length > 1) {
    words.forEach(w => {
      if (w.length > 2) tabooWords.push(w.toUpperCase())
    })
  }
  
  // Add some generic related words based on word characteristics
  const wordLower = word.toLowerCase()
  
  // Common associations
  if (wordLower.includes('man') || wordLower.includes('woman')) {
    tabooWords.push('PERSON', 'HUMAN', 'PEOPLE')
  }
  if (wordLower.includes('phone') || wordLower.includes('ipad') || wordLower.includes('computer')) {
    tabooWords.push('DEVICE', 'TECHNOLOGY', 'SCREEN')
  }
  if (wordLower.includes('food') || wordLower.includes('eat')) {
    tabooWords.push('FOOD', 'EAT', 'MEAL')
  }
  
  // Fill remaining slots with generic words
  const genericTaboo = ['THING', 'ITEM', 'OBJECT', 'WORD', 'NAME', 'DESCRIBE', 'SAY', 'TELL', 'USE', 'RELATED']
  while (tabooWords.length < 5) {
    const random = genericTaboo[Math.floor(Math.random() * genericTaboo.length)]
    if (!tabooWords.includes(random) && random !== upperWord) {
      tabooWords.push(random)
    }
  }
  
  return tabooWords.slice(0, 5)
}

// Determine difficulty based on word characteristics
function getDifficulty(word: string): { difficulty: string; points: number; rare?: string } {
  const length = word.length
  const wordCount = word.split(' ').length
  const hasSpecialChars = /[^a-zA-Z\s]/.test(word)
  
  // Very long or complex words
  if (length > 15 || wordCount > 3) {
    return { difficulty: 'hard', points: 25 + Math.floor(Math.random() * 6), rare: Math.random() > 0.7 ? 'rare' : undefined }
  }
  
  // Long words or multi-word
  if (length > 10 || wordCount > 2) {
    return { difficulty: 'medium', points: 15 + Math.floor(Math.random() * 6) }
  }
  
  // Medium length
  if (length > 6 || wordCount > 1) {
    return { difficulty: 'medium', points: 12 + Math.floor(Math.random() * 4) }
  }
  
  // Short/simple words
  return { difficulty: 'easy', points: 8 + Math.floor(Math.random() * 4) }
}

// Process the word list
export function generateWordDatabase() {
  const words = rawWordList
    .split('\n')
    .map(w => w.trim())
    .filter(w => w.length > 0)
  
  return words.map(word => {
    const upperWord = word.toUpperCase()
    const tabooWords = generateTabooWords(word)
    const difficultyInfo = getDifficulty(word)
    
    return {
      word: upperWord,
      taboo: tabooWords,
      ...difficultyInfo
    }
  })
}

// Export the processed word database
export const wordDatabase = generateWordDatabase()
